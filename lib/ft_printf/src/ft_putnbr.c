#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	l;

	l = 0;
	if (n > 2147483647)
		return (0);
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		l += ft_putchar('-');
		n = ~(n - 1);
	}
	if (n >= 10)
		l += ft_putnbr(n / 10);
	l += ft_putchar((n % 10) + '0');
	return (l);
}
