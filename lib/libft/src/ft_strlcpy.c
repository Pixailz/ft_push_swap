#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	char			*tmp;
	size_t			i;

	tmp = (char *)src;
	while (*tmp)
		tmp++;
	i = tmp - src;
	if (!size)
		return (i);
	tmp = dst;
	while (--size && *src)
		*dst++ = *src++;
	*dst = 0;
	return (i);
}
