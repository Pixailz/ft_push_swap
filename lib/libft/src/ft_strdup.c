#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*tmp;
	char	*ptr;

	tmp = (char *)s;
	while (*tmp)
		tmp++;
	ptr = (char *)malloc((tmp - s) + 1);
	tmp = ptr;
	while (*s && tmp)
		*tmp++ = *s++;
	if (ptr)
		*tmp = 0;
	return (ptr);
}
