#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char	*tmp;
	char	*tsrc;

	tmp = dest;
	tsrc = (char *)src;
	while (*tsrc)
		*tmp++ = *tsrc++;
	*tmp = 0;
	return (dest);
}
